"""
Write a python script to convert a number into str type
"""

# defining a "int" type variable with value 324
int_var = 324

# converting "int" type variable int "str" type
# with the help of "str()" function
# which converts specified argument into "str" type
str_var = str(int_var)

# printing "value" and type of "str" variable
print("Value of str variable", str_var, "and type =>", type(str_var))